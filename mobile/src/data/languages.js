// ===== HELPER FUNCTIONS =====

export const getLanguageById = (id) => languages.find(l => l.id === id);

export const getActiveLanguages = () => languages.filter(l => l.isActive);

export const getLanguagesByTier = (tier) => languages.filter(l => l.tier === tier && l.isActive);

export const getDialectsOf = (parentId) => languages.filter(l => l.parentLanguage === parentId);

export const getRTLLanguages = () => languages.filter(l => l.textDirection === 'rtl');

export const searchLanguages = (query) => {
  const q = query.toLowerCase();
  return languages.filter(l => 
    l.nameEnglish.toLowerCase().includes(q) ||
    l.nameNative.includes(q) ||
    l.cuisineName.toLowerCase().includes(q)
  );
};

export const getLanguageCount = () => languages.length;

export const getActiveLanguageCount = () => getActiveLanguages().length;

export default languages;
